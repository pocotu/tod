function calcularAnualidades() {
    var opcionSeleccionada = document.getElementById("opcionesAnualidades").value;
    // Aquí se puede escribir el código para calcular la anualidad según la opción seleccionada
    var resultado = "";
    if(opcionSeleccionada === "opcion1") {
        resultado = "Resultado opción 1";
    } else if(opcionSeleccionada === "opcion2") {
        resultado = "Resultado opción 2";
    } else if(opcionSeleccionada === "opcion3") {
        resultado = "Resultado opción 3";
    }
    document.getElementById("el-resultado").innerHTML = resultado;
}

