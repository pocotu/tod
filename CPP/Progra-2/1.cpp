// Suma de dos numeros

#include <iostream>
using namespace std;

int main()
{
    int a, b, suma;
    cout << "Ingrese el primer numero: ";
    cin >> a;
    cout << "Ingrese el segundo numero: ";
    cin >> b;
    suma = a + b;
    cout << "La suma de los dos numeros es: " << suma << endl;
    return 0;
}

